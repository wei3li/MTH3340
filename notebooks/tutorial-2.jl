md"Let us create a function that given a number of cells $N$, the finite element order $p$ and the analytical solution, computes the approximated finite element solution. It is pretty much the Poisson driver."

# ╔═╡ ff7d4017-3a12-4d58-8705-e68252feb817
function compute_solution(N,order,u)

  f(x) = - Δ(u)(x)

  domain = (0,1)
  partition = (N,)
  model = CartesianDiscreteModel(domain,partition)
  trian = get_triangulation(model)

  degree = 2(order - 1)
  dΩ = Measure(trian,degree)
  dΩ_super = Measure(trian, 4(degree + 1))

  reffe = ReferenceFE(lagrangian,Float64,order)

  V = TestFESpace(
     model,reffe;
     conformity=:H1,
     dirichlet_tags="boundary")

  U = TrialFESpace(V,u)

  a(u,v) = ∫( ∇(v)⋅∇(u) )dΩ
  l(v) = ∫( v*f )dΩ_super

  op = AffineFEOperator(a,l,U,V)

  uh = solve(op)

  e = u - uh

  eh1 = sqrt(sum( ∫( e*e + ∇(e)⋅∇(e) )*dΩ_super ))

  return uh, eh1
end #function

# ╔═╡ 11e00a96-14c3-4f4a-bb8f-c857f331b6a1
md"Let me stress the fact that the computation of the error requires a more
accurate quadrature than for the finite element part. Otherwise, we would have an integration error in the computation of the numerical error that would polute the convergence orders."

# ╔═╡ 19118399-f016-4aa7-b6a7-a15b352b23be
md"Now, let us create another function that performs $h$ and $p$-refinement.
 It includes an external loop in which we increase the order ($p$=refinement) and an internal
 loop in which, for every order, runs the problem for different cell numbers ($h$-refinement).
 We must provide the set of orders and number of cells we want to explore
 in  the arrays `ps` and `ncells` and the analytical solution we want for our problem in `u`.
 It returns three arrays of arrays. First, we have as many arrays as orders computed. For every order, we have another array with as many entries as the number of cells used. These three arrays contain error in H1 seminorm, number of dofs and the finite element solution."

# ╔═╡ cc823a67-3092-4f09-8d7a-cd133630f471
function hp_refinement(u,ps,ncells)

  eh1s_hp = []
  ndofs_hp = []
  uhs_hp = []

  for p in ps

    eh1s_h = Float64[]
    ndofs_h = Float64[]
    uhs_h = []

    for N in ncells

      uh, eh1 = compute_solution(N,p,u)
      push!(uhs_h,uh)
      push!(eh1s_h,eh1)

      ndof = Float64(N*p)
      push!(ndofs_h,ndof)

    end

    push!(eh1s_hp,eh1s_h)
    push!(ndofs_hp,ndofs_h)
    push!(uhs_hp,uhs_h)

  end

  return eh1s_hp, ndofs_hp, uhs_hp

end

