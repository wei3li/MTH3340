 $x$ and $y$ axis, e.g., log10, or identity."

# ╔═╡ 4755ea99-f0f7-4ac4-b2d2-73ad315dacec
function compute_slope(ndofs,error,funx,funy)
  slope = Float64[]
  for i in 1:length(ndofs)
    x = funx.(ndofs[i])
    y = funy.(error[i])
    linreg = hcat(fill!(similar(x), 1), x) \ y
    push!(slope,linreg[2])
  end
  return slope
end

